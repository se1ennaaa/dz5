public class Boss {
    private int halth;
    private int damage;
    private String defense;

    public int getHalth() {
        return halth;
    }

    public void setHalth(int halth) {
        this.halth = halth;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getDefense() {
        return defense;
    }

    public void setDefense(String defense) {
        this.defense = defense;
    }
}
